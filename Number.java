    public void printNum() {
        i++;
        int num = Integer.parseInt(number);
        sum = sum + number1;
        Average = sum / i;
        if (num % 2 == 0) {
            System.out.print(num);

            even = number1;
        } else {
            odd = number1;
        }
        txtEven.setText("" + even);
        txtOdd.setText("" + odd);
        txtSum.setText("" + sum);
        txtAvg.setText("" + Average);
    }

