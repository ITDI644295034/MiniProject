    private void btnPlusActionPerformed(java.awt.event.ActionEvent evt) {                                        

        number = txtNum.getText();
        if (number.length() > 0) {
            cbxSign.setEditable(false);
            String Sign = (String) cbxSign.getSelectedItem();
            if (Sign == ",") {
                text = text + number + Sign;
                str = text.split(",");
                number1 = Integer.valueOf(str[i]);
                printNum();
            }
            if (Sign == ":") {
                text = text + number + Sign;
                str = text.split(":");
                number1 = Integer.valueOf(str[i]);
                printNum();
            }
            txrResult.setText(text);
            txtNum.setText("");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Please complete the information first.");

        }

    }                                       

